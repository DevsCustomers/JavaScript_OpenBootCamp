// If else + if else

let nota = 4;

if ( nota === 5 ) {
    console.log("En hora buena, eres sobresaliente");
} else if ( nota === 4 ){
    console.log("Buen trabajo, pero podrias haberlo hecho mejor");
} else if ( nota === 3 ) {
    console.log("Has obtenido una nota promedio");
} else if ( nota === 2 ) {
    console.log("No has aprobado");
} else if ( nota === 1) {
    console.log("No has estudiado nada, trabaja un poco mas para la proxima");
} else {
    console.log("ERROR, introduce una nota entre el 1 al 5");
}